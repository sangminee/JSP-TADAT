package community;

public class Community {
	
	private int communityID;
	private String userID;
	private String communityTitle;
	private String communityContent;
	private String communityDate;
	private int communityAvaliable;
	
	public int getCommunityID() {
		return communityID;
	}
	public void setCommunityID(int communityID) {
		this.communityID = communityID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getCommunityTitle() {
		return communityTitle;
	}
	public void setCommunityTitle(String communityTitle) {
		this.communityTitle = communityTitle;
	}
	public String getCommunityContent() {
		return communityContent;
	}
	public void setCommunityContent(String communityContent) {
		this.communityContent = communityContent;
	}
	public String getCommunityDate() {
		return communityDate;
	}
	public void setCommunityDate(String communityDate) {
		this.communityDate = communityDate;
	}
	public int getCommunityAvaliable() {
		return communityAvaliable;
	}
	public void setCommunityAvaliable(int communityAvaliable) {
		this.communityAvaliable = communityAvaliable;
	}
	
	
}
