package company;

public class Company {

	private int companyID;
	private String userID;
	private String companyName;
	private String companyField;
	private String companyWorkingHours;
	private String companySalary;
	private String companyContent;
	private String companyDate;
	private int companyAvaliable;
	
	public int getCompanyID() {
		return companyID;
	}
	public void setCompanyID(int companyID) {
		this.companyID = companyID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getCompanyField() {
		return companyField;
	}
	public void setCompanyField(String companyField) {
		this.companyField = companyField;
	}
	public String getCompanyWorkingHours() {
		return companyWorkingHours;
	}
	public void setCompanyWorkingHours(String companyWorkingHours) {
		this.companyWorkingHours = companyWorkingHours;
	}
	public String getCompanySalary() {
		return companySalary;
	}
	public void setCompanySalary(String companySalary) {
		this.companySalary = companySalary;
	}
	public String getCompanyContent() {
		return companyContent;
	}
	public void setCompanyContent(String companyContent) {
		this.companyContent = companyContent;
	}
	public String getCompanyDate() {
		return companyDate;
	}
	public void setCompanyDate(String companyDate) {
		this.companyDate = companyDate;
	}
	public int getCompanyAvaliable() {
		return companyAvaliable;
	}
	public void setCompanyAvaliable(int companyAvaliable) {
		this.companyAvaliable = companyAvaliable;
	}
	
}
